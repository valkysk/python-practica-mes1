# Variables in Python

my_string_variable = "My String Variable"
print(my_string_variable)  # This will print: My String Variable

my_int_variable = 25
print(my_int_variable)  # This will print: 25

my_bool_variable = False
print(my_bool_variable)  # This will print: False

print(my_string_variable, my_int_variable, my_bool_variable)