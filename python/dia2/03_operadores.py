# Operadores de comparación
print("¿num1 es igual a num2?", num1 == num2)
print("¿num1 es diferente de num2?", num1 != num2)
print("¿num1 es mayor que num2?", num1 > num2)
print("¿num1 es menor que num2?", num1 < num2)
print("¿num1 es mayor o igual que num2?", num1 >= num2)
print("¿num1 es menor o igual que num2?", num1 <= num2)
